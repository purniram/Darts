public class DartCircles {

    int dartCircle1;
    int dartCircle2;
    int dartCircle3;

    public DartCircles (int dartCircle1, int dartCircle2, int dartCircle3)
    {
        this.dartCircle1 = dartCircle1;
        this.dartCircle2 = dartCircle2;
        this.dartCircle3 = dartCircle3;
    }

    public DartCircles()
    {
         dartCircle1 = 1;
         dartCircle2 = 5;
         dartCircle3 = 10;
    }
}
